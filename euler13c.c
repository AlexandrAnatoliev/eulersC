int get_summ(char num_arr1[], char num_arr2[], int len_num)
// функция складывает два больших числа, представленных в виде массивов
// параметры:	num_arr1[]  - указатель на массив с первым числом (делаем на 5 цифр длиннее LEN_ARR)
//              num_arr2[]  - указатель на массив со вторым числом
//              len_num     - длина первого числа
// return:                  - длина первого числа после сложения со вторым
{
    int rest = 0;
    int cnt = 0;

    while(cnt < len_num || rest)
    {
        rest += num_arr1[cnt];                                  // складываем одинаковые разряды двух чисел и остаток 

        if(cnt < LEN_NUM)                                       // не выходим за массив
            rest += num_arr2[cnt];

        num_arr1[cnt++] = rest % 10;                            // заносим в массив младший разряд числа
        rest /= 10;                                             // переносим остаток числа в следующий разряд
    }
    
    return cnt;
}

